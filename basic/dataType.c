#include <stdio.h>

int main()
{
    int myIntegerValue = 100;
    char myCharacter = 'A';
    float myFloatValue = 100.123;
    double myDoubleValue = 100.45678;

    printf("myIntegerValue=%d\n", myIntegerValue);
    printf("myCharacter=%c\n", myCharacter);
    printf("myFloatValue=%f\n", myFloatValue);
    printf("myDoubleValue=%f\n", myDoubleValue);
    return 0;
}

/*
myIntegerValue=100
myCharacter=A
myFloatValue=100.123001
myDoubleValue=100.456780
*/